# Creating a tuple using round brackets
a = (2,3,4,5,'bhavya')  
print(a)
b = ()                 # EMPTY TUPLE
print(b)
c = (2, )              # TUPLE WITH ONE ELEMENT NEEDS A COMMA AT THE END
print(c)
d = (2)                # NOT A TUPLE
print(d)
 
