name = "Bhavya"
print(name[0:3])      # Prints index 0,1 and 2 only that is Bha
print(name[1:3])      # Prints index 1 and 2 only that is ha
print(name[:4])       # Prints index 0,1,2 and 3 that is Bhav
print(name[1:])       # Print index 1,2,3,4 and 5 that is havya
print(len(name))      # Print length of a string
print(name[-4:-1])    # Print index 2,3 and 4 that is avy
print(name[0:5:2])    # Have a skip value 2. Print index 0,2 and 4 that is Bay
print(name[0::3])     # Have a skip value 3. Print index 0 and 3 that is Bv